/**
 * Next.js project scaffolder for Mode B (interactive site with Framer Motion).
 * Creates a complete Next.js 15 + TypeScript + Tailwind + shadcn/ui project.
 */

import { mkdirSync, writeFileSync } from 'fs';
import { join } from 'path';
import { brandToShadcnVars, brandToTailwindConfig } from './color-mapper.mjs';
import {
  getLayout,
  getProviders,
  getGlobalCSS,
  getPage,
  getHeroComponent,
  getSectionComponent,
  getNavComponent,
  getFooterComponent,
  getAnimatedCounter,
  getTailwindConfig,
  getNextConfig,
  getTsConfig,
  getPostcssConfig,
  getUtils,
} from './nextjs-templates.mjs';
import {
  getCardComponent,
  getBadgeComponent,
  getTableComponent,
} from '../templates/nextjs/shadcn-components.mjs';

/**
 * Generate a complete Next.js project structure.
 * @param {object} parsed - Parsed markdown structure
 * @param {object} brand - Brand config
 * @param {object} effectsConfig - Effects configuration from effects-config.mjs
 * @param {string} outputPath - Root directory for the project
 */
export function generateNextJSProject(parsed, brand, effectsConfig, outputPath) {
  console.log(`\n📦 Generating Next.js project at ${outputPath}...\n`);

  // Create directory structure
  createDirectoryStructure(outputPath);

  // Generate shadcn CSS vars for globals.css
  const shadcnVars = brandToShadcnVars(brand, 'dark');

  // Write root config files
  writeRootConfigs(outputPath, brand);

  // Write app/ directory files
  writeAppFiles(outputPath, parsed, brand, shadcnVars, effectsConfig);

  // Write components/ directory files
  writeComponentFiles(outputPath, effectsConfig);

  // Write lib/ directory files
  writeLibFiles(outputPath);

  console.log('\n✅ Next.js project generated successfully!\n');
  console.log('📋 Next steps:');
  console.log('   1. cd', outputPath);
  console.log('   2. npm install');
  console.log('   3. npm run dev');
  console.log('   4. Open http://localhost:3000\n');
}

/**
 * Create the complete directory structure.
 */
function createDirectoryStructure(root) {
  const dirs = [
    'app',
    'components',
    'components/ui',
    'lib',
  ];

  dirs.forEach((dir) => {
    const fullPath = join(root, dir);
    mkdirSync(fullPath, { recursive: true });
  });
}

