/**
 * Write app/ directory files.
 */
function writeAppFiles(root, parsed, brand, shadcnVars, effectsConfig) {
  const appDir = join(root, 'app');

  // layout.tsx
  writeFileSync(join(appDir, 'layout.tsx'), getLayout(brand));

  // providers.tsx
  writeFileSync(join(appDir, 'providers.tsx'), getProviders(brand));

  // globals.css
  writeFileSync(join(appDir, 'globals.css'), getGlobalCSS(shadcnVars));

  // page.tsx
  writeFileSync(join(appDir, 'page.tsx'), getPage(parsed, brand, effectsConfig));
}

/**
 * Write components/ directory files.
 */
function writeComponentFiles(root, effectsConfig) {
  const componentsDir = join(root, 'components');
  const uiDir = join(componentsDir, 'ui');

  // Main components
  writeFileSync(join(componentsDir, 'hero.tsx'), getHeroComponent(effectsConfig));
  writeFileSync(join(componentsDir, 'section.tsx'), getSectionComponent(effectsConfig));
  writeFileSync(join(componentsDir, 'nav.tsx'), getNavComponent());
  writeFileSync(join(componentsDir, 'footer.tsx'), getFooterComponent());

  // Animated counter (conditional)
  if (effectsConfig.animatedCounters) {
    const counterContent = getAnimatedCounter(effectsConfig);
    if (counterContent) {
      writeFileSync(join(componentsDir, 'animated-counter.tsx'), counterContent);
    }
  }

  // shadcn/ui components
  writeFileSync(join(uiDir, 'card.tsx'), getCardComponent());
  writeFileSync(join(uiDir, 'badge.tsx'), getBadgeComponent());
  writeFileSync(join(uiDir, 'table.tsx'), getTableComponent());
}

/**
 * Write lib/ directory files.
 */
function writeLibFiles(root) {
  const libDir = join(root, 'lib');

  // utils.ts (cn helper)
  writeFileSync(join(libDir, 'utils.ts'), getUtils());
}
